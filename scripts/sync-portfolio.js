/**
 * sync-portfolio.js
 *
 * Scans the parent folder (Google Drive) for project directories containing info.json,
 * copies images into public/images/[slug]/, and generates data/projects-generated.js.
 *
 * Usage:  node scripts/sync-portfolio.js
 *         npm run sync
 */

const fs = require('fs');
const path = require('path');

// ── Paths ──
const MAIN_DIR = path.resolve(__dirname, '..');
const PORTFOLIO_DIR = path.resolve(MAIN_DIR, '..');
const PUBLIC_IMAGES = path.join(MAIN_DIR, 'public', 'images');
const DATA_DIR = path.join(MAIN_DIR, 'data');
const OUTPUT_FILE = path.join(DATA_DIR, 'projects-generated.js');

// ── Config ──
const IMAGE_EXTS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif']);
const SKIP_FOLDERS = new Set(['Main', '.claude', 'EUNSEOLEE', '.git', '.vercel', 'node_modules']);

// ── Hardcoded project slugs (already have dedicated pages) ──
const HARDCODED_SLUGS = new Set(['tofug', 'benson', 'bodyguard', 'gagga', 'soldam']);

function isImageFile(filename) {
  return IMAGE_EXTS.has(path.extname(filename).toLowerCase());
}

function getModifiedTime(filepath) {
  try {
    return fs.statSync(filepath).mtimeMs;
  } catch {
    return 0;
  }
}

function ensureDir(dir) {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
}

function copyIfNewer(src, dest) {
  const srcTime = getModifiedTime(src);
  const destTime = getModifiedTime(dest);
  if (srcTime > destTime) {
    fs.copyFileSync(src, dest);
    return true;
  }
  return false;
}

function scanAndSync() {
  console.log('');
  console.log('=== Portfolio Sync ===');
  console.log(`Scanning: ${PORTFOLIO_DIR}`);
  console.log('');

  ensureDir(DATA_DIR);

  // Read all directories in portfolio root
  let entries;
  try {
    entries = fs.readdirSync(PORTFOLIO_DIR, { withFileTypes: true });
  } catch (err) {
    console.error('Failed to read portfolio directory:', err.message);
    process.exit(1);
  }

  const projects = [];
  let totalCopied = 0;
  let totalSkipped = 0;

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    if (SKIP_FOLDERS.has(entry.name)) continue;

    const folderPath = path.join(PORTFOLIO_DIR, entry.name);
    const infoPath = path.join(folderPath, 'info.json');

    // Skip folders without info.json
    if (!fs.existsSync(infoPath)) {
      continue;
    }

    // Read and parse info.json
    let info;
    try {
      const raw = fs.readFileSync(infoPath, 'utf-8');
      info = JSON.parse(raw);
    } catch (err) {
      console.warn(`  Warning: Failed to parse ${entry.name}/info.json — ${err.message}`);
      continue;
    }

    if (!info.slug) {
      console.warn(`  Warning: ${entry.name}/info.json has no slug, skipping`);
      continue;
    }

    const slug = info.slug;
    const destDir = path.join(PUBLIC_IMAGES, slug);
    ensureDir(destDir);

    // Copy image files
    let folderCopied = 0;
    let folderSkipped = 0;

    const files = fs.readdirSync(folderPath);
    for (const file of files) {
      if (!isImageFile(file)) continue;

      const srcFile = path.join(folderPath, file);
      const destFile = path.join(destDir, file);

      if (copyIfNewer(srcFile, destFile)) {
        folderCopied++;
      } else {
        folderSkipped++;
      }
    }

    totalCopied += folderCopied;
    totalSkipped += folderSkipped;

    // Store folder name for reference
    info._sourceFolder = entry.name;
    info._hasHardcodedPage = HARDCODED_SLUGS.has(slug);

    projects.push(info);

    const status = folderCopied > 0
      ? `${folderCopied} copied, ${folderSkipped} up-to-date`
      : `${folderSkipped} up-to-date`;
    console.log(`  [${slug}] ${entry.name} — ${status}`);
  }

  // Sort projects by order, then by year descending
  projects.sort((a, b) => {
    const orderDiff = (a.order || 99) - (b.order || 99);
    if (orderDiff !== 0) return orderDiff;
    return (b.year || '').localeCompare(a.year || '');
  });

  // Generate data/projects-generated.js
  const output = `// Auto-generated by sync-portfolio.js — DO NOT EDIT MANUALLY
// Last sync: ${new Date().toISOString()}

const projects = ${JSON.stringify(projects, null, 2)};

module.exports = projects;
`;

  fs.writeFileSync(OUTPUT_FILE, output, 'utf-8');

  console.log('');
  console.log(`Done! ${projects.length} projects synced (${totalCopied} images copied, ${totalSkipped} up-to-date)`);
  console.log(`Generated: data/projects-generated.js`);
  console.log('');
}

scanAndSync();
